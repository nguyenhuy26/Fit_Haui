    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.rowAtPoint(evt.getPoint());
        String MaVT = (String) table.getValueAt(row, 0);
        String TenVT = (String) table.getValueAt(row, 1);
        int SoLuong = (int) table.getValueAt(row, 3);
        long KinhPhi = (long) table.getValueAt(row, 4);
        String TinhTrang = (String) table.getValueAt(row, 2);
        txtmavt.setText(MaVT);
        txttenvt.setText(TenVT);
        if (TinhTrang.equals("Mới")) {
            cbx_tinhtrang.setSelectedIndex(0);
        }
        if (TinhTrang.equals("Cũ")) {
            cbx_tinhtrang.setSelectedIndex(1);
        }
        if (TinhTrang.equals("Hư Hỏng")) {
            cbx_tinhtrang.setSelectedIndex(2);
        }
        txtsoluong.setText(String.valueOf(SoLuong));
        txtKinhPhi.setText(String.valueOf(KinhPhi));


    }//GEN-LAST:event_tableMouseClicked

    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        LoadFile();
        String MaVT = txtmavt.getText();
        String TenVT = txttenvt.getText();
        String TinhTrang;
        int tinhtrang = cbx_tinhtrang.getSelectedIndex();
        if (tinhtrang == 0 || tinhtrang == -1) {
            TinhTrang = "Mới";
        } else if (tinhtrang == 1) {
            TinhTrang = "Cũ";
        } else {
            TinhTrang = "Hư Hỏng";
        }

        String SoLuong = txtsoluong.getText();
        String KinhPhi = txtKinhPhi.getText();
        int Soluong1 = 0;
        long KinhPhi1 = 0;
        boolean check = false;
        for (QuanLiCoSoVatChat_Class item : dsvc) {
            if (item.getMaVatTu().equals(MaVT)) {
                check = true;
                break;
            }

        }
        if (check) {
            JOptionPane.showMessageDialog(rootPane, "Trùng 1 mã vật tư");

        } else {
            if (MaVT.isEmpty() || TenVT.isEmpty() || SoLuong.isEmpty() || KinhPhi.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Không được để trống 1 trong các dữ liệu");
            } else {
                boolean check1 = false;
                try {
                    Soluong1 = Integer.parseInt(SoLuong);
                    KinhPhi1 = Long.parseLong(KinhPhi);
                    check1 = true;
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Số lượng / Kinh phí phải là 1 con số");
                }
                if (check1) {
                    String KhanHiem;
                    if (Soluong1 <= 5) {
                        KhanHiem = "Rất Khan Hiêm";
                    } else if (Soluong1 >= 6 && Soluong1 <= 10) {
                        KhanHiem = "Khan Hiếm";
                    } else {
                        KhanHiem = "Bình THường";
                    }
                    dsvc.add(new QuanLiCoSoVatChat_Class(MaVT, TenVT, TinhTrang, Soluong1, KinhPhi1, KhanHiem));
                    try {
                        FileOutputStream fos = new FileOutputStream("CSVC.txt");
                        ObjectOutputStream oos = new ObjectOutputStream(fos);
                        oos.writeObject(dsvc);
                        oos.close();
                        fos.close();
                    } catch (FileNotFoundException e) {
                        e.printStackTrace();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                    txtmavt.setText("");
                    txttenvt.setText("");
                    txtKinhPhi.setText("");
                    txtsoluong.setText("");
                    cbx_tinhtrang.setSelectedIndex(0);
                    reset();
                    JOptionPane.showMessageDialog(rootPane, "Them Thanh Cong");
                }
            }
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

