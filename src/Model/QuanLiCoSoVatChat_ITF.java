    public void LoadFile() {
        try {
            FileInputStream fis = new FileInputStream("CSVC.txt");
            ObjectInputStream ois = new ObjectInputStream(fis);
            var resultList = (ArrayList) ois.readObject();
            dsvc = resultList;
            ois.close();
            fis.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
    }

    public void reset() {
        LoadFile();
        JTable table = new JTable();
        DefaultTableModel model = new DefaultTableModel();
        Object[] columnNames = {"Mã Vật Tư", "Tên Vật Tư", "Tình Trạng", "Số lượng", "Kinh Phí", "Độ khan hiếm"};
        model.setColumnIdentifiers(columnNames);
        Object[][] rowData = new Object[dsvc.size()][7];
        for (int i = 0; i < dsvc.size(); i++) {
            rowData[i][0] = dsvc.get(i).getMaVatTu();
            rowData[i][1] = dsvc.get(i).getTenVatTu();
            rowData[i][2] = dsvc.get(i).getTinhTrang();
            rowData[i][3] = dsvc.get(i).getSoLuong();
            rowData[i][4] = dsvc.get(i).getKinhPhi();
            rowData[i][5] = dsvc.get(i).getDoKhanHiem();
        }
        model.setDataVector(rowData, columnNames);
        this.table.setModel(model);
        this.add(new JScrollPane(table));
        this.pack();
        this.setVisible(true);
    }

